package baiTap1.Bai6;

public class Items {
    private int idItems;
    private String nameItems;
    private String categoryItems;
    private int priceItems;

    public int getIdItems() {
        return idItems;
    }

    public void setIdItems(int idItems) {
        this.idItems = idItems;
    }

    public String getNameItems() {
        return nameItems;
    }

    public void setNameItems(String nameItems) {
        this.nameItems = nameItems;
    }

    public String getCategoryItems() {
        return categoryItems;
    }

    public void setCategoryItems(String categoryItems) {
        this.categoryItems = categoryItems;
    }

    public int getPriceItems() {
        return priceItems;
    }

    public void setPriceItems(int priceItems) {
        this.priceItems = priceItems;
    }
}

package baiTap1.Bai6;

import java.util.Date;

public class StaffImport {
    private int idStaffImport;
    private String sexStaffImport;
    private Date  dateStaffImport;
    private  int seniorityStaffImport;


    public int getIdStaffImport() {
        return idStaffImport;
    }

    public void setIdStaffImport(int idStaffImport) {
        this.idStaffImport = idStaffImport;
    }

    public String getSexStaffImport() {
        return sexStaffImport;
    }

    public void setSexStaffImport(String sexStaffImport) {
        this.sexStaffImport = sexStaffImport;
    }

    public Date getDateStaffImport() {
        return dateStaffImport;
    }

    public void setDateStaffImport(Date dateStaffImport) {
        this.dateStaffImport = dateStaffImport;
    }

    public int getSeniorityStaffImport() {
        return seniorityStaffImport;
    }

    public void setSeniorityStaffImport(int seniorityStaffImport) {
        this.seniorityStaffImport = seniorityStaffImport;
    }
}
